def cal():
    a=int(input("enter the number"))
    b=int(input("enter the number"))
    operation=(input("enter the operation"))
    if operation=="+":
        print(a+b)
    elif operation=="-":
        print(a-b)
    elif operation=="*":
        print(a*b)
    elif operation=="/":
        print(a/b)
    else:
        print("error")
